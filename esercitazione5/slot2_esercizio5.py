# Esercizio 5: Scrivere la funzione ‘perimetro_quadrato’ che prenda
# in ingresso il lato di un quadrato e restituisca il valore del perimetro.
# Scrivere poi la funzione ‘area_quadrato’ che prenda in ingresso il lato
# e restituisca il valore dell’area del quadrato.

#   Nome: perimetro_quadrato
#
#   Argomenti:
#       - lato: il lato del quadrato
#
#   Valore di ritorno: perimetro del quadrato
def perimetro_quadrato(lato):
    return lato * 4


#   Nome: area_quadrato
#
#   Argomenti:
#       - lato: lato del quadrato
#
#   Valore di ritorno: area del quadrato
def area_quadrato(lato):
    return lato ** 2
