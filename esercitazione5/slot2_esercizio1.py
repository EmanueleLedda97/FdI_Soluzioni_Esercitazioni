# Esercizio 1: Definire la funzione ‘somma’ che prenda
# in ingresso due numeri e ne calcoli la somma

# Utilizzo la parola chiave def per definire una funzione
#   Nome: somma
#
#   Argomenti:
#       - num1: il primo numero da sommare
#       - num2: il secondo numero da sommare
#
#   Valore di ritorno: la somma tra i due numeri
def somma(num1, num2):
    return num1 + num2
